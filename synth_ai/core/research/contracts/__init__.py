def __getattr__(name: str) -> object:
    if name not in _ECONOMICS_EXPORTS:
        raise AttributeError(name)
    value = getattr(
        importlib.import_module("synth_ai.core.research.contracts.economics"),
        name,
    )
    globals()[name] = value
    return value

